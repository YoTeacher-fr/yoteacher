    initialize() {
        const savedLanguage = localStorage.getItem('language');
        
        if (savedLanguage && this.supportedLanguages.includes(savedLanguage)) {
            this.currentLanguage = savedLanguage;
        } else {
            const browserLanguage = navigator.language.split('-')[0];
            if (this.supportedLanguages.includes(browserLanguage)) {
                this.currentLanguage = browserLanguage;
            }
        }
        
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => {
                this.init();
            });
        } else {
            this.init();
        }
    }

    init() {
        this.updatePage();
        this.addEventListeners();
    }

    addEventListeners() {
        const desktopSwitcher = document.getElementById('languageSwitcherDesktop');
        if (desktopSwitcher) {
            desktopSwitcher.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                this.toggleLanguage();
            });
        }
        
        const mobileSwitcher = document.getElementById('languageSwitcherMobile');
        if (mobileSwitcher) {
            mobileSwitcher.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                this.toggleLanguage();
                
                const mobileMenu = document.getElementById('mobileMenu');
                if (mobileMenu && mobileMenu.classList.contains('active')) {
                    mobileMenu.classList.remove('active');
                }
            });
        }
    }

    toggleLanguage() {
        this.currentLanguage = this.currentLanguage === 'fr' ? 'en' : 'fr';
        localStorage.setItem('language', this.currentLanguage);
        
        this.updatePage();
        
        window.dispatchEvent(new CustomEvent('language:changed', { 
            detail: { language: this.currentLanguage } 
        }));
    }

    updatePage() {
        document.documentElement.lang = this.currentLanguage;
        this.applyTranslations();
        this.updateLanguageSwitchers();
    }

    applyTranslations() {
        // Traduire les √©l√©ments avec data-i18n
        document.querySelectorAll('[data-i18n]').forEach((element) => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getTranslation(key);
            if (translation && translation !== key) {
                if (translation.includes('<')) {
                    element.innerHTML = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });

        // Traduire les placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            const translation = this.getTranslation(key);
            if (translation && translation !== key) {
                element.placeholder = translation;
            }
        });

        // Traduire les titles
        document.querySelectorAll('[data-i18n-title]').forEach(element => {
            const key = element.getAttribute('data-i18n-title');
            const translation = this.getTranslation(key);
            if (translation && translation !== key) {
                element.title = translation;
            }
        });
        
        // Mettre √† jour les titres de page
        const title = document.querySelector('title[data-i18n]');
        if (title) {
            const key = title.getAttribute('data-i18n');
            const translation = this.getTranslation(key);
            if (translation && translation !== key) {
                title.textContent = translation;
            }
        }
    }

    updateLanguageSwitchers() {
        const languageSwitchers = document.querySelectorAll(
            '.language-switcher span:last-child, ' +
            '.mobile-language span:last-child, ' +
            '.language-switcher-mobile span'
        );
        
        languageSwitchers.forEach(switcher => {
            switcher.textContent = this.currentLanguage === 'fr' ? 'EN' : 'FR';
            
            const parent = switcher.closest('.language-switcher, .mobile-language, .language-switcher-mobile');
            if (parent) {
                parent.title = this.currentLanguage === 'fr' 
                    ? this.getTranslation('header.switch_to_english')
                    : this.getTranslation('header.switch_to_french');
            }
        });
    }

    getTranslation(key, params = []) {
        let translation = '';
        
        if (this.translations[this.currentLanguage] && this.translations[this.currentLanguage][key]) {
            translation = this.translations[this.currentLanguage][key];
        } else if (this.translations['fr'] && this.translations['fr'][key]) {
            translation = this.translations['fr'][key];
        } else {
            return key;
        }
        
        // Remplacer les param√®tres {0}, {1}, etc.
        if (params && params.length > 0) {
            params.forEach((param, index) => {
                translation = translation.replace(`{${index}}`, param);
            });
        }
        
        return translation;
    }

    getCurrentLanguage() {
        return this.currentLanguage;
    }

    setLanguage(language) {
        if (this.supportedLanguages.includes(language)) {
            this.currentLanguage = language;
            localStorage.setItem('language', language);
            this.updatePage();
            return true;
        }
        return false;
    }
